// Peça ao usuário inserr dois números e guarde-os em variável diferentes (num1 e num2)
const num1 = Number(prompt("Insira o primeiro número"))
const num2 = Number(prompt("Insira o segundo número"))

//IMPRIMA NO CONSOLE
console.log(`Números digitados são, num1 = ${num1} e num2 = ${num2}`)

// A soma do primeiro com o segundo
console.log("Soma", num1 + num2)

// A subtração do primeiro pelo segundo
console.log("Subtração", num1 - num2)

// A multiplicação do primeiro pelo segundo
console.log("Multiplicação", num1 * num2)

// A divisão do primeiro pelo segundo
console.log("Divisão", num1 / num2)

// O resto da divisão do primeiro pelo segundo

console.log("Resto da Divisão", num1 % num2)
